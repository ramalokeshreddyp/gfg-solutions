class Solution {
  public:
    int findKRotation(vector<int> &arr) {
        // Code Here
        int index=max_element(arr.begin(),arr.end())-arr.begin();
        index++;
        if(index==arr.size()){
            return 0;
        }
        else{
            return index;
        }
    }
};


