class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
         int max_val=*max_element(arr.begin(),arr.end());
        int max=-1;
        for(auto it=arr.begin();it!=arr.end();it++){
            if(max<*it&&*it!=max_val){
                max=*it;
            }
        }
        return max;
    }
};

